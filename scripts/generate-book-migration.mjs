import fs from "node:fs/promises";
import path from "node:path";
import { randomUUID } from "node:crypto";
import { fileURLToPath } from "node:url";
import { parseBookDescription } from "./book-description-parser.mjs";
import {
  compareRu,
  convertPoundsToKilograms,
  extractDescription,
  getFolderPath,
  getOldItemSection,
  migrateAssetPath,
  readLevelDocuments,
  toInteger,
  toNumber
} from "./generate-material-migration.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const systemRoot = path.resolve(__dirname, "..");
const dataRoot = path.resolve(systemRoot, "..", "..");
const OLD_WORLD_ROOT = path.join(dataRoot, "fallout-old");
const MACRO_OUTPUT_DIR = path.join(systemRoot, "scripts", "migration-macros", "books");
const MACRO_FILE = "01-import-book-items.js";
const ROOT_FOLDER = "MAW Импорт книг";
const BOOK_FOLDER_PREFIX = "Книги / ";

async function main() {
  const [items, folders] = await Promise.all([
    readLevelDocuments(path.join(OLD_WORLD_ROOT, "data", "items")),
    readLevelDocuments(path.join(OLD_WORLD_ROOT, "data", "folders"))
  ]);
  const folderById = new Map(folders.map(folder => [folder._id, folder]));

  const candidates = items
    .filter(item => getFolderPath(item.folder, folderById).startsWith(BOOK_FOLDER_PREFIX))
    .map(item => ({
      item,
      folderPath: getFolderPath(item.folder, folderById),
      parsed: parseBookDescription(extractDescription(item))
    }))
    .sort((left, right) => (
      compareRu(left.folderPath, right.folderPath)
      || compareRu(left.item.name, right.item.name)
      || left.item._id.localeCompare(right.item._id)
    ));

  await fs.mkdir(MACRO_OUTPUT_DIR, { recursive: true });

  const records = [];
  const unparsed = [];
  for (const entry of candidates) {
    if (!entry.parsed) {
      unparsed.push(`${entry.item._id}\t${entry.item.name}\t${entry.folderPath}`);
      continue;
    }
    records.push(await createBookMigrationRecord(entry.item, entry.folderPath, entry.parsed));
  }

  const buildStamp = new Date().toISOString();
  await fs.writeFile(
    path.join(MACRO_OUTPUT_DIR, MACRO_FILE),
    buildBookMacro(records, buildStamp),
    "utf8"
  );
  await fs.writeFile(
    path.join(MACRO_OUTPUT_DIR, "README.md"),
    buildReadme(records.length, candidates.length, buildStamp),
    "utf8"
  );
  await fs.writeFile(
    path.join(MACRO_OUTPUT_DIR, "00-PASTE-INTO-FOUNDRY-MACRO.js"),
    buildPasteMacro(),
    "utf8"
  );

  console.log(`Book items found: ${candidates.length}`);
  console.log(`Parsed with oneTimeUse function: ${records.length}`);
  console.log(`Unparsed descriptions: ${unparsed.length}`);
  if (unparsed.length) console.log(unparsed.join("\n"));
  console.log(`Macro written: ${path.join(MACRO_OUTPUT_DIR, MACRO_FILE)}`);
}

async function createBookMigrationRecord(item, folderPath, parsed) {
  const img = await migrateAssetPath(item.img);
  const changes = (parsed.changes ?? []).map(change => ({
    id: randomUUID(),
    key: change.key,
    type: change.type,
    value: change.value,
    phase: change.phase,
    priority: change.priority
  }));

  return {
    id: item._id,
    name: item.name,
    img,
    folderPath: normalizeBookFolderPath(folderPath).split(" / ").filter(Boolean),
    oldType: item.type,
    oldFolderPath: folderPath,
    oldImg: item.img ?? "",
    system: {
      description: "",
      quantity: Math.max(1, toInteger(item.system?.quantity, 1)),
      maxStack: 1,
      itemCategory: getOldItemSection(item) || "Журналы",
      weight: convertPoundsToKilograms(item.system?.weight?.value ?? item.system?.weight),
      price: Math.max(0, toNumber(item.system?.price?.value ?? item.system?.price, 0)),
      equipped: false,
      locked: false,
      placement: {
        mode: "inventory",
        equipmentSlot: "",
        weaponSet: "",
        weaponSlot: "",
        limbKey: "",
        constructPartOrder: 0,
        x: 1,
        y: 1,
        width: 1,
        height: 1,
        rotated: false
      },
      functions: {
        oneTimeUse: {
          enabled: true,
          repeatApplicationBlocked: true,
          changes
        }
      },
      craft: {
        mode: "craft",
        nodes: [],
        links: [],
        viewport: { x: 0, y: 0, zoom: 1 },
        disassembly: { nodes: [], links: [], viewport: { x: 0, y: 0, zoom: 1 } },
        recipes: []
      }
    }
  };
}

function normalizeBookFolderPath(folderPath) {
  return String(folderPath ?? "").replace(/^Книги \/ /, "");
}

function buildBookMacro(records, buildStamp) {
  return `// Generated by systems/fallout-maw/scripts/generate-book-migration.mjs
// ${buildStamp}

const BOOK_ITEMS = ${JSON.stringify(records, null, 2)};

const SYSTEM_ID = "fallout-maw";
const ROOT_FOLDER = ${JSON.stringify(ROOT_FOLDER)};
const FLAG_SCOPE = "fallout-maw";
const FLAG_KEY = "bookMigration";
const MATERIAL_FLAG_KEY = "materialMigration";
const JUNK_FLAG_KEY = "junkMigration";
const TOOL_FLAG_KEY = "toolMigration";
const FIRST_AID_FLAG_KEY = "firstAidMigration";

await runBookImport();

async function runBookImport() {
  if (game.system.id !== SYSTEM_ID) {
    ui.notifications.error("Этот макрос рассчитан на систему fallout-maw.");
    return;
  }

  await ensureItemCategories(Array.from(new Set(BOOK_ITEMS
    .map(entry => String(entry.system?.itemCategory ?? "").trim())
    .filter(Boolean))));

  const touched = [];
  for (const entry of BOOK_ITEMS) {
    const folderId = await ensureFolderPath([ROOT_FOLDER, ...entry.folderPath]);
    const existing = findExistingMigrationItem(entry);
    const data = buildItemData(entry, folderId);
    if (existing) {
      const updateData = foundry.utils.deepClone(data);
      delete updateData._id;
      await existing.update(updateData);
      touched.push(existing);
    } else {
      touched.push(await createItemWithPreferredId(data));
    }
  }

  ui.notifications.info(\`Импорт книг: обработано \${touched.length}.\`);
  console.log("Book import", { touched: touched.length });
}

async function createItemWithPreferredId(data) {
  try {
    return await Item.create(data);
  } catch (error) {
    console.warn("Не удалось создать предмет с исходным id, создаю с новым id.", data._id, error);
    const fallback = foundry.utils.deepClone(data);
    delete fallback._id;
    return Item.create(fallback);
  }
}

function buildItemData(entry, folderId) {
  const system = foundry.utils.deepClone(entry.system);
  return {
    _id: entry.id,
    name: entry.name,
    type: "gear",
    img: entry.img,
    folder: folderId,
    system,
    flags: {
      [FLAG_SCOPE]: {
        [FLAG_KEY]: {
          oldId: entry.id,
          oldType: entry.oldType,
          oldFolderPath: entry.oldFolderPath,
          oldImg: entry.oldImg,
          sourceWorld: "fallout-old"
        }
      }
    }
  };
}

function findExistingMigrationItem(entry) {
  const byFlag = game.items.find(item => item.getFlag(FLAG_SCOPE, FLAG_KEY)?.oldId === entry.id);
  if (byFlag) return byFlag;
  const byMaterialFlag = game.items.find(item => item.getFlag(FLAG_SCOPE, MATERIAL_FLAG_KEY)?.oldId === entry.id);
  if (byMaterialFlag) return byMaterialFlag;
  const byJunkFlag = game.items.find(item => item.getFlag(FLAG_SCOPE, JUNK_FLAG_KEY)?.oldId === entry.id);
  if (byJunkFlag) return byJunkFlag;
  const byToolFlag = game.items.find(item => item.getFlag(FLAG_SCOPE, TOOL_FLAG_KEY)?.oldId === entry.id);
  if (byToolFlag) return byToolFlag;
  const byFirstAidFlag = game.items.find(item => item.getFlag(FLAG_SCOPE, FIRST_AID_FLAG_KEY)?.oldId === entry.id);
  if (byFirstAidFlag) return byFirstAidFlag;
  const byId = game.items.get(entry.id);
  if (byId?.name === entry.name) return byId;
  return null;
}

async function ensureFolderPath(parts) {
  let parentId = null;
  for (const name of parts) {
    let folder = game.folders.find(candidate => (
      candidate.type === "Item"
      && candidate.name === name
      && getFolderParentId(candidate) === parentId
    ));
    if (!folder) folder = await Folder.create({ name, type: "Item", folder: parentId });
    parentId = folder.id;
  }
  return parentId;
}

function getFolderParentId(folder) {
  return folder.folder?.id ?? folder.folder ?? null;
}

async function ensureItemCategories(labels) {
  try {
    const settings = foundry.utils.deepClone(game.settings.get(SYSTEM_ID, "itemCategories") ?? { categories: [] });
    settings.categories ??= [];
    const existing = new Set(settings.categories.map(category => String(category.label ?? "")));
    for (const label of labels) {
      if (!label || existing.has(label)) continue;
      settings.categories.push({ label });
      existing.add(label);
    }
    await game.settings.set(SYSTEM_ID, "itemCategories", settings);
  } catch (error) {
    console.warn("Не удалось обновить категории предметов.", error);
  }
}
`;
}

