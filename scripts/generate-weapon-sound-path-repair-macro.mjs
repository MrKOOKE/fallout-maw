import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const systemRoot = path.resolve(__dirname, "..");
const soundRoot = path.join(systemRoot, "audio", "Weapon_Sounds");
const outputPath = path.join(systemRoot, "scripts", "migration-macros", "gear", "00-repair-weapon-sound-paths.js");

const SYSTEM_ID = "fallout-maw";
const SOUND_PREFIX = "systems/fallout-maw/audio/Weapon_Sounds/";

const LEGACY_SOUND_ALIASES = {
  "rifle_assault.mp3": "systems/fallout-maw/audio/Weapon_Sounds/Orugee_2/AssaultRifle_Fire_2D.mp3",
  "wpn_flamer_fire.mp3": "systems/fallout-maw/audio/Weapon_Sounds/Or/wpn_flamer_fire_2d_lpm.mp3",
  "chansaw1.mp3": "systems/fallout-maw/audio/Weapon_Sounds/Orugee_2/Saw1.mp3"
};

const soundPaths = await collectSoundPaths(soundRoot);
const soundPathSet = new Set(soundPaths);
const pathsByBasename = new Map();
for (const soundPath of soundPaths) {
  const basename = normalizeKey(path.posix.basename(soundPath));
  const paths = pathsByBasename.get(basename) ?? [];
  paths.push(soundPath);
  pathsByBasename.set(basename, paths);
}

const uniquePathByBasename = Object.fromEntries(
  Array.from(pathsByBasename.entries())
    .filter(([, paths]) => paths.length === 1)
    .map(([basename, paths]) => [basename, paths[0]])
);

for (const [oldName, nextPath] of Object.entries(LEGACY_SOUND_ALIASES)) {
  if (!soundPathSet.has(nextPath)) {
    throw new Error(`Legacy alias target is missing: ${oldName} -> ${nextPath}`);
  }
}

const buildStamp = new Date().toISOString();
const macro = `// Fallout-MaW one-shot repair: weapon sound paths only.
// Generated by systems/fallout-maw/scripts/generate-weapon-sound-path-repair-macro.mjs
// ${buildStamp}

const SYSTEM_ID = ${JSON.stringify(SYSTEM_ID)};
const SOUND_PREFIX = ${JSON.stringify(SOUND_PREFIX)};
const EXISTING_SOUND_PATHS = new Set(${JSON.stringify(soundPaths, null, 2)});
const UNIQUE_SOUND_PATH_BY_BASENAME = ${JSON.stringify(uniquePathByBasename, null, 2)};
const LEGACY_SOUND_ALIASES = ${JSON.stringify(LEGACY_SOUND_ALIASES, null, 2)};

if (game.system.id !== SYSTEM_ID) {
  ui.notifications.error("This macro is only for the fallout-maw system.");
  return;
}

let updated = 0;
let skipped = 0;
let repairedPaths = 0;
let unresolvedPaths = 0;
let errors = 0;
const unresolved = [];
const repairs = [];

for (const item of game.items.contents) await repairItemDocument(item, "world");
for (const actor of game.actors.contents) {
  for (const item of actor.items.contents) await repairItemDocument(item, actor.name);
}

if (unresolved.length) {
  console.warn("weapon sound path repair unresolved", unresolved);
  console.table(unresolved);
}
if (repairs.length) console.table(repairs);

ui.notifications.info(
  \`Weapon sound path repair: updated items \${updated}, repaired paths \${repairedPaths}, unresolved \${unresolvedPaths}, unchanged \${skipped}, errors \${errors}.\`
);
console.log("weapon sound path repair", { updated, skipped, repairedPaths, unresolvedPaths, errors, repairs, unresolved });

async function repairItemDocument(item, ownerName = "world") {
  if (item.type !== "gear") return;
  const functions = item.system?.functions ?? {};
  const updates = {};
  const localRepairs = [];
  collectSoundPathUpdates(functions, "system.functions", item, ownerName, updates, localRepairs);
  if (!Object.keys(updates).length) {
    skipped += 1;
    return;
  }
  try {
    await item.update(updates);
    updated += 1;
    repairedPaths += localRepairs.length;
    repairs.push(...localRepairs);
  } catch (error) {
    errors += 1;
    console.error("weapon sound path repair failed", { ownerName, itemId: item.id, itemName: item.name, updates, error });
  }
}

function collectSoundPathUpdates(value, path, item, ownerName, updates, localRepairs) {
  if (!value || typeof value !== "object") return;
  for (const [key, entry] of Object.entries(value)) {
    const entryPath = \`\${path}.\${key}\`;
    if ((key === "attackSoundPath" || key === "explosionSoundPath") && typeof entry === "string") {
      const repaired = repairSoundPath(entry);
      if (repaired === entry) continue;
      if (!repaired) {
        unresolvedPaths += 1;
        unresolved.push({ owner: ownerName, item: item.name, itemId: item.id, path: entryPath, value: entry });
        continue;
      }
      updates[entryPath] = repaired;
      localRepairs.push({ owner: ownerName, item: item.name, itemId: item.id, path: entryPath, from: entry, to: repaired });
      continue;
    }
    if (entry && typeof entry === "object") collectSoundPathUpdates(entry, entryPath, item, ownerName, updates, localRepairs);
  }
}

function repairSoundPath(rawPath = "") {
  const pathValue = normalizeSoundPath(rawPath);
  if (!pathValue) return "";
  if (!pathValue.startsWith(SOUND_PREFIX)) return rawPath;
  if (EXISTING_SOUND_PATHS.has(pathValue)) {
    return pathValue === rawPath ? rawPath : pathValue;
  }

  const basename = normalizeKey(pathValue.split("/").pop() ?? "");
  const alias = LEGACY_SOUND_ALIASES[basename];
  if (alias) return alias;
  return UNIQUE_SOUND_PATH_BY_BASENAME[basename] ?? "";
}

function normalizeSoundPath(rawPath = "") {
  let pathValue = String(rawPath ?? "").trim();
  if (!pathValue) return "";
  try {
    pathValue = decodeURIComponent(pathValue);
  } catch (_error) {
    // Keep raw path.
  }
  pathValue = pathValue.replace(/\\\\/g, "/");
  pathValue = pathValue
    .replace(/^systems\\/fallout-maw\\/icons\\/Weapon_Sounds/i, SOUND_PREFIX.slice(0, -1))
    .replace(/^systems\\/fallout-maw\\/icons\\/WEAPON_SOUNDS/i, SOUND_PREFIX.slice(0, -1));
  if (/^Weapon_Sounds\\//i.test(pathValue)) pathValue = \`systems/fallout-maw/audio/\${pathValue}\`;
  return pathValue;
}

function normalizeKey(value = "") {
  return String(value ?? "").trim().toLocaleLowerCase("ru");
}
`;

await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(outputPath, macro, "utf8");
console.log(`weapon sound path repair macro: ${outputPath}`);
console.log(`sound paths: ${soundPaths.length}`);
console.log(`unique basenames: ${Object.keys(uniquePathByBasename).length}`);

async function collectSoundPaths(root) {
  const output = [];
  await walk(root);
  return output.sort((left, right) => left.localeCompare(right, "ru"));

  async function walk(directory) {
    const entries = await fs.readdir(directory, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(directory, entry.name);
      if (entry.isDirectory()) {
        await walk(fullPath);
        continue;
      }
      if (!entry.isFile() || !isAudioFile(entry.name)) continue;
      const relativePath = path.relative(systemRoot, fullPath).split(path.sep).join("/");
      output.push(`systems/fallout-maw/${relativePath}`);
    }
  }
}

function isAudioFile(fileName) {
  return /\.(?:mp3|ogg|wav|flac|m4a)$/i.test(fileName);
}

function normalizeKey(value = "") {
  return String(value ?? "").trim().toLocaleLowerCase("ru");
}
